export function translateLearningText(value: string | null | undefined, lang: Lang): string {
    if (!value) return '';
    if (lang === 'vi') return value;
    if (EXACT_EN[value]) return EXACT_EN[value];

    let translated = value;
    for (const [from, to] of PHRASE_EN) {
        translated = translated.split(from).join(to);
    }

    return EXACT_EN[translated] ?? translated;
}

export function translateLearningList(values: string[], lang: Lang): string[] {
    return values.map((value) => translateLearningText(value, lang));
}

export function formatLearningGrade(grade: number, lang: Lang): string {
    return lang === 'vi' ? `Lớp ${grade}` : `Grade ${grade}`;
}

export function formatLearningGradeList(grades: number[], lang: Lang): string {
    return lang === 'vi' ? `Lớp ${grades.join(', ')}` : `Grades ${grades.join(', ')}`;
}

export function formatLearningCount(count: number, lang: Lang): string {
    return lang === 'vi' ? `${count} câu` : `${count} questions`;
}
